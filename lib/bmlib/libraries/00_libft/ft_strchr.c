#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != (char)c && *s != '\0')
		s++;
	if (*s == (char)c || (*s == '\0' && (char)c == '\0'))
		return ((char *)s);
	else
		return (0);
}
